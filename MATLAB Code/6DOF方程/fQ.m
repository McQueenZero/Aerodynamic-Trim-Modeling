function Q = fQ(h, V)
% 由ISA简表推出的当前飞行高度与空气密度的关系函数
rho0=1.225;T0=288.15;
if h<=11000
    T=T0-0.0065*h;
    rho=rho0*(T/T0)^4.25588;
elseif h>11000&&h<=20000
    T=216.65;
    rho=0.36392*exp((-h+11000)/6341.62);
else
    T=216.65+0.001*(h-20000);
    rho=0.088035*(T/216.65)^-35.1632;
end
Q = 0.5 * rho * V * V;